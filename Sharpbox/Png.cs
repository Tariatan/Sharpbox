namespace Sharpbox;

public static class Png
{
    public static void CreatePngFile(string fileName)
    {
        // Create Png file with one black dot
        byte[] pngHeader =
        [
            0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
            0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
            0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
            0x08, 0x00, 0x00, 0x00, 0x00, 0x3A, 0x7E, 0x9B,
            0x55,
            0x00, 0x00, 0x00, 0x0d, 0x49, 0x44, 0x41, 0x54,
            0x08, 0x1d, 0x01, 0x02, 0x00, 0xfd, 0xff, 0x00,
            0x00, 0x00, 0x02, 0x00, 0x01, 0xcd, 0xe3, 0xd1,
            0x2b,
            0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44,
            0xae, 0x42, 0x60, 0x82
        ];

        using var fileStream = new FileStream(fileName, FileMode.Create, FileAccess.Write);
        fileStream.Write(pngHeader, 0, pngHeader.Length);
    }
}
